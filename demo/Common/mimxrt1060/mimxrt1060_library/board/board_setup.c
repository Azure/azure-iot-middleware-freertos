void board_setup(void)
{
    /* Enable cycle counter for tx_exection_profile.c use */
    CoreDebug->DEMCR |= CoreDebug_DEMCR_TRCENA_Msk;
    DWT->LAR = 0xC5ACCE55;
    DWT->CTRL |= DWT_CTRL_CYCCNTENA_Msk;
    
    BOARD_ConfigMPU();
    BOARD_InitPins();
    BOARD_BootClockRUN();
    BOARD_InitDebugConsole();
   
    CLOCK_InitEnetPll(&config);

    eth_setup();

    hardware_rand_initialize();

    srand(hardware_rand());
}

int hardware_rand()
{

int data;
status_t status;

    status = TRNG_GetRandomData(TRNG, &data, sizeof(data));
    if(status)
        return status;

    return data;


}
void hardware_rand_initialize()
{
trng_config_t trngConfig;
status_t status;

    /* Initialize TRNG configuration structure to default.*/
    TRNG_GetDefaultConfig(&trngConfig);
    /* Set sample mode of the TRNG ring oscillator to Von Neumann, for better random data.
     * It is optional.*/
    trngConfig.sampleMode = kTRNG_SampleModeVonNeumann;

    /* Initialize TRNG */
    status = TRNG_Init(TRNG, &trngConfig);

    if(status)
        return;

}



